    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCity.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please selece a community to remove.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        Community selectedCommunity = (Community) model.getValueAt(selectedRowIndex, 0);
        
        admin.city.deleteCommunity(selectedCommunity);
        JOptionPane.showMessageDialog(this, "Remove Community successfully.");
        populateCityTable();
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCity.getSelectedRow();
        if(selectedRowIndex<0){
            JOptionPane.showMessageDialog(this, "Please selece a community to update.");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        Community selectedCommunity = (Community) model.getValueAt(selectedRowIndex, 0);
        
        UpdateCommunityJPanel updateCommu = new UpdateCommunityJPanel(userProcessContainer, admin, selectedCommunity);
        userProcessContainer.add("UpdateCommunityJPanel",updateCommu);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_btnUpdateActionPerformed


