    public void populateCityTable() {
        DefaultTableModel model = (DefaultTableModel) tblCity.getModel();
        model.setRowCount(0);
        
        HashMap<Integer, Community> cm = admin.city.getCommunityMap();
        
        for(Community c: cm.values()){
            Object[] row = new Object[2];
            row[0] = c;  // when show the name string of vs, override the method to show serial number
            row[1] = c.getId();
            
            model.addRow(row);
        }
        txtCityName.setText(admin.city.getCityName());
    }
